t_list	*ft_lstlast(t_list *lst)
{
	if (!lst)
		return (NULL);
	while (lst != NULL)
	{
		if (lst->next == NULL)
			return (lst);
		printf("content: %d\n", lst->content);
		lst = lst->next;
	}
	return (lst);
}
